/** \brief suma los valores ingresados por el usuario
 *
 * \param a int operando 1 ingresado por el usuario.
 * \param b int operando 2 ingresado por el usuario.
 * \return int  el resultado de la suma de los 2 operandos.
 *
 */
int suma(int a,int b);

/** \brief realiza el calculo de resta
 *
 * \param operando 1 ingresado por el usuario.
 * \param operando 2 ingresado por el usuario.
 * \return devuelve el resultado de la resta.
 *
 */
int resta (int a, int b);

/** \brief realiza la operacion de la division entre 2 enteros y la convierte a flotante.
 *
 * \param a int operando 1 ingresado por el usuario.
 * \param b int operando 2 ingresado por el usuario.
 * \return float devuelve el resultado de la division.
 *
 */
float division(int a,int b);


/** \brief realiza la operacion de la multiplicacion.
 *
 * \param a int operando 1 ingresado por el usuario.
 * \param b int operando 2 ingresado por el usuario.
 * \return int devuleve el resultado de la multiplicacion.
 *
 */
int multiplicacion(int a,int b);


/** \brief Calcula el factorial de un numero.
 *
 * \param a int numero ingresado por el usuario
 * \param b int numero ingresado por el usuario
 * \return devuelve el resultado de el factorial de cada operando ingresado.
 *
 */
int factorial(int a);
